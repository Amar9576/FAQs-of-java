/*Positive, Negative, or Zero
Write a Java program that checks if a given number is positive, negative,
or zero using if-else-if.*/


public class PositiveNegative {
    public static void main (String[]args){
        int number =-0;
        if(number>0){
            System.out.println("No is Positive: "+number);
        }
        else if(number<0){
            System.out.println("no is Negative: "+number);
        }
        else{
            System.out.println("no is Zero: "+number);
        }
    

    }
    
}
